#arthematic
a = input("enter a value")
b = input("enter b value")
print(int(a) + int(b))
print(int(a) - int(b))
print(int(a) * int(b))
print(int(a) % int(b))
print(int(a) / int(b))
print(int(a) // int(b))

#logical

print(int(a) & int(b))
print(int(a) | int(b))
print(int(a) ^ int(b))

#bitwise

print(int(a) | int(b))
print(int(a) ^ int(b))
print(int(a) >> int(b))
print(int(a) << int(b))

#relational

print(int(a) <= int(b))
print(int(a) >= int(b))
print(int(a) == int(b))
print(int(a) != int(b))
print(int(a) < int(b))
print(int(a) > int(b))

#identity
print(int(a) is int(b))
print(int(a) is not int(b))
print(type(a))

